//------------------------------------------------------
uint32
HanamiFontTable::codeToNo(uint32 code) const 
{
	uint32	newcode;

	newcode = 0;
	if ((code >= '0') && (code <= '9')) {
		newcode  =code - '0' + 1;
	} else {
		if((code >= 'A') && (code <= 'Z')) {
			newcode = code - 'A' + 11;
		} else {
			if((code >= 'a') && (code <= 'z')) {
				newcode = code - 'a' + 37;
			} else {
				if(code == '-') {
					newcode = 63;
				}
			}
		}
	}
	return newcode;
}

